const getUserChannelProfile = asyncHandler(async (req, res) => {
    const { username, id } = req.params;

    // Check if both username and id are missing
    if (!username?.trim() && !id) {
        throw new ApiError(400, "username or id is required");
    }

    // Build the match query based on provided parameters
    const matchQuery = {};
    if (username?.trim()) {
        matchQuery.username = username.toLowerCase();
    }
    if (id) {
        matchQuery._id = id; // Assumes id is a valid ObjectId string
    }

    const channel = await User.aggregate([
        {
            $match: matchQuery
        },
        {
            $lookup: {
                from: "subscriptions",
                localField: "_id",
                foreignField: "channel",
                as: "subscribers"
            }
        },
        {
            $lookup: {
                from: "subscriptions",
                localField: "_id",
                foreignField: "subscriber",
                as: "subscribedTo"
            }
        },
        {
            $addFields: {
                subscribersCount: {
                    $size: "$subscribers"
                },
                channelsSubscribedToCount: {
                    $size: "$subscribedTo"
                },
                isSubscribed: {
                    $cond: {
                        if: { $in: [req.user?._id, "$subscribers.subscriber"] },
                        then: true,
                        else: false
                    }
                }
            }
        },
        {
            $project: {
                fullName: 1,
                username: 1,
                subscribersCount: 1,
                channelsSubscribedToCount: 1,
                isSubscribed: 1,
                avatar: 1,
                coverImage: 1,
                email: 1
            }
        }
    ]);

    if (!channel?.length) {
        throw new ApiError(404, "Channel does not exist");
    }

    return res
        .status(200)
        .json(
            new ApiResponse(200, channel[0], "User channel fetched successfully")
        );
});



const getWatchHistory = asyncHandler(async(req, res) => {
    // console.log(req.body, "getWatchHistory start");

    const { page = 1, limit = 12, sortBy = 'watchedAt', sortType = 'desc' } = req.body;

    const skip = (page - 1) * limit;
    const sortDirection = sortType === 'desc' ? -1 : 1;

    const user = await User.aggregate([
        {
            $match: {
                _id: new mongoose.Types.ObjectId(req.user._id)
            }
        },
        {
            $lookup: {
                from: "videos",
                localField: "watchHistory",
                foreignField: "_id",
                as: "watchHistory",
                pipeline: [
                    {
                        $lookup: {
                            from: "users",
                            localField: "owner",
                            foreignField: "_id",
                            as: "owner",
                            pipeline: [
                                {
                                    $project: {
                                        fullName: 1,
                                        username: 1,
                                        avatar: 1
                                    }
                                }
                            ]
                        }
                    },
                    {
                        $addFields: {
                            owner: {
                                $first: "$owner"
                            }
                        }
                    },
                    {
                        $sort: {
                            [sortBy]: sortDirection 
                        }
                    },
                    {
                        $skip: skip 
                    },
                    {
                        $limit: limit 
                    }
                ]
            }
        }
    ]);

    const totalWatchedVideos = user[0].watchHistory.length;

    // To get the total count of videos (before pagination), run a second query without skip/limit
    const totalVideosResult = await User.aggregate([
        {
            $match: {
                _id: new mongoose.Types.ObjectId(req.user._id)
            }
        },
        {
            $lookup: {
                from: "videos",
                localField: "watchHistory",
                foreignField: "_id",
                as: "watchHistory"
            }
        }
    ]);

    const totalVideosCount = totalVideosResult[0].watchHistory.length;

    return res
        .status(200)
        .json(
            new ApiResponse(
                200,
                {
                    watchHistory: user[0].watchHistory,
                    totalWatchedVideos,
                    totalVideosCount
                },
                "Watch history fetched successfully"
            )
        );
});



